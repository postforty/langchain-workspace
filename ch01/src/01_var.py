# 변수
# 기본 자료형
int_var = 10
print(int_var)
float_var = 3.14
print(float_var)
str_var = "Hello, World!"
print(str_var)
bool_var = True
print(bool_var)

# 컬렉션 자료형
list_var = [1, 2, 3, 4, 5] # mutable
print(list_var[3])
tuple_var = (10, 20, 30) # immutable
print(tuple_var[1])
dict_var = {"name": "Alice", "age": 30} # 순서없음, 키 중복없음
print(dict_var["name"])
print(dict_var["age"])
set_var = {1, 2, 3, 3, 3, 4, 5} # 순서없음, 중복없음
print(set_var)